# Дан символ C. Вывести два символа, первый из которых предшествует символу C в
# кодовой таблице, а второй следует за символом C.
def adjacent_chars(C):
    # Получаем числовое представление символа
    num = ord(C)

    # Проверяем, что символ находится в допустимом диапазоне
    if num - 1 < 0 or num + 1 > 0x10FFFF:
        return "Недопустимый символ"

    # Возвращаем символы, находящиеся до и после исходного символа
    return chr(num - 1), chr(num + 1)

# Пример использования
prev_char, next_char = adjacent_chars('C')
print(f"Предыдущий символ: {prev_char}, Следующий символ: {next_char}")